// LLENAR EL "ESCENARIO" DE PANELES
/*
1. limpiar
2. entrar a  nivel
3. leer fila
4. leer clase del panel
*/
function generarEscenario()
{	
	for (let y=0; y < nivel.length; y++)
    {
		xb = 0 - retraso
		for (let x =0; x < nivel[y].length; x++)
        {
            switch (nivel[y][x]){
                case 0:
                    c = "white";
                    break;
                case 1: 
                    c = "green";
                    break;
            }
            bloques.push( new elemento(15, 15, xb, yb, false, "bloque", 0, 0, 0, 0, c) );
			xb += 15
		}
		yb += 15
	}
	xb = retraso;
	yb = 0
    for (bloque of bloques){
        bloque.dibujar();
    }
}