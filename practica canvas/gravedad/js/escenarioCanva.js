// VARIABLES DE COLOR
var c0 = "white";
var c1 = "green";
var c2 = "green";

function generarEscenario()
{	
	for (let y=0; y < nivel.length; y++)
    {
		xb = 0 - retraso
		for (let x =0; x < nivel[y].length; x++)
        {
            switch (nivel[y][x]){
                case 0: c = c0;
                    break;
                case 1: c = c1;
                    break;
                case 2: c = c2;
                    break;
                case 3: c = c2;
                    break;
            }
            bloques.push( new elemento(15, 15, xb, yb, false, "bloque", nivel[y][x], 0, 0, 0, 0, c) );
			xb += 15
		}
		yb += 15
	}
	xb = retraso;
	yb = 0
    for (bloque of bloques){
        bloque.dibujar();
    }
}